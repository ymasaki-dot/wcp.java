package Variable01;

public class IF01 {
	public static void main(String[] args) {
		int number = 7;
		if (number < 5) {
			System.out.println(number + " は5未満です");
		}
		if (number < 10) {
			System.out.println(number + " は5以上です");
		}
		if (number < 15) {
			System.out.println(number + " は15未満です");
		}
	}
}
