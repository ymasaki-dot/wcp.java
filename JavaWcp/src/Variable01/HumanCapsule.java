package Variable01;

public class HumanCapsule {
	private String name;
	private int age;
	
	public HumanCapsule(String name, int age) {
		this.name = name;
		this.age = age;
	}
	
	public String getName() {
		return this.name;
	}
	
	public String setName(String name) {
		this.name = name;
	}
	
	public int getAge() {
		return this.age;
	}

}
